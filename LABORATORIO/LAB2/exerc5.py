# A fórmula para calcular a área de uma circunferência é: area = π * raio2 . Considerando que π = 3.14159, efetue o cálculo da área para o valor de raio obtido do usuário. Exiba o resultado.


# Por exemplo:

# Input	Resultado
# 2
# Digite o valor do raio: 2
# 12.56636

raio = int(input("Digite o valor do raio: "))

area = 3.14159*(raio**2)
print(area)
